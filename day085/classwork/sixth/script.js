//ნაწილი და დააბრუნოთ ფუნქციიდან გამოიყენეთ for ციკლი


function manualSlice(arr,start,end){
    for(let i = start; i < end; i++){
        return arr.slice(start,end)
        
    }
}
console.log(manualSlice([1,2,3,4,5], 2,5))


